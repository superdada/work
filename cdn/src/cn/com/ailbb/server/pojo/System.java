public class System {
    private double version = 1.01;
    private long visits = 0;
    private String languageType;
    private int minAccessTime = 1000*10;// 最小记录访问间隔10秒
    private int minHandlerTime = 1000*60; // 最小请求数据库系统版本信息

    public long getVisits() {
        return visits;
    }

    public void setVisits(long visits) {
        visits = visits;
    }

    public double getVersion() {
        return version;
    }

    public void setVersion(double version) {
        this.version = version;
    }

    public String getLanguageType() {
        return languageType;
    }

    public void setLanguageType(String languageType) {
        this.languageType = languageType;
    }

    public int getMinAccessTime() {
        return minAccessTime;
    }

    public void setMinAccessTime(int minAccessTime) {
        this.minAccessTime = minAccessTime;
    }

    public int getMinHandlerTime() {
        return minHandlerTime;
    }

    public void setMinHandlerTime(int minHandlerTime) {
        this.minHandlerTime = minHandlerTime;
    }
}
